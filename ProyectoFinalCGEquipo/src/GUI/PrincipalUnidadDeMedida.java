    private void btnGuardarAcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarAcActionPerformed

        String id=tfid.getText();
        String nombre = tfNombreAc.getText();
        String descripcion = taDescripcionAC.getText();
       
        UnidadDeMedidaPOJO pojo = new UnidadDeMedidaPOJO();
        pojo.setIdUnidadDeMedida(Integer.parseInt(id));
        pojo.setNombre_unidadDeMedida(nombre);
        pojo.setDescripcion_unidadDeMedida(descripcion);
       
        boolean x = UnidadDeMedidaJDBC.insertar(pojo);
        if (x == true) {
            JOptionPane.showMessageDialog(null, "Éxito");
            limpiarComponentes();
            dialogoActualizaControlUnidadDeMedida.setVisible(false);
            cargarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "Error");
        }        

// TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarAcActionPerformed

