    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        int filaSeleccionada = tbControlUnidadDeMedida.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "primero selecciona una fila");
        } else {
            String id = tbControlUnidadDeMedida.getValueAt(filaSeleccionada, 0).toString();
            String nombre = tbControlUnidadDeMedida.getValueAt(filaSeleccionada, 1).toString();
            String descripcion = tbControlUnidadDeMedida.getValueAt(filaSeleccionada, 2).toString();
            tfid.setText(id);
            tfNombreAc.setText(nombre);
            taDescripcionAC.setText(descripcion);
            dialogoActualizaControlUnidadDeMedida.setSize(389, 364);
            dialogoActualizaControlUnidadDeMedida.setLocationRelativeTo(null);
            dialogoActualizaControlUnidadDeMedida.setVisible(true);
        }
// TODO add your handling code here:
    }//GEN-LAST:event_btnActualizarActionPerformed

